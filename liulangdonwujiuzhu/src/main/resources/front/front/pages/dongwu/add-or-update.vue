<script>
    import wPicker from "@/components/w-picker/w-picker.vue";

    export default {
        data() {
            return {
                cross: '',
                ro:{
					dongwuName: false,
					dongwuPhoto: false,
					dongwuTypes: false,
					yonghuId: false,
					dongwuClicknum: false,
					dongwuContent: false,
					qiuzhuzhuangtaiTypes: false,
					dongwuDelete: false,
					insertTime: false,
					createTime: false,
        },
            ruleForm: {
					dongwuName: '',
					dongwuPhoto: '',
					dongwuTypes: '',//数字
					dongwuValue: '',//数字对应的值
					yonghuId: '',
					dongwuClicknum: '',
					dongwuContent: '',
					qiuzhuzhuangtaiTypes: '',//数字
					qiuzhuzhuangtaiValue: '',//数字对应的值
					dongwuDelete: '',
					insertTime: '',
					createTime: '',
            },
            // 登陆用户信息
            user: {},
            dongwuTypesOptions: [],
            dongwuTypesIndex : 0,
            qiuzhuzhuangtaiTypesOptions: [],
            qiuzhuzhuangtaiTypesIndex : 0,
            yonghuOptions: [],//用户
            yonghuIndex : 0,//用户下标

        }
        },
        components: {
            wPicker
        },
        computed: {
            baseUrl() {
                return this.$base.url;
            },
        },
        async onLoad(options) {
            let yonghuParams = {
                page: 1,
                limit: 100,
            }
            let yonghuData = await this.$api.page(`yonghu`, yonghuParams);
            this.yonghuOptions = yonghuData.data.list;
		/*下拉框*/
			let dongwuTypesParams = {
                page: 1,
                limit: 100,
                dicCode: 'dongwu_types',
            }
			let dongwuTypes = await this.$api.page(`dictionary`, dongwuTypesParams);
			this.dongwuTypesOptions = dongwuTypes.data.list
		/*下拉框*/
			let qiuzhuzhuangtaiTypesParams = {
                page: 1,
                limit: 100,
                dicCode: 'qiuzhuzhuangtai_types',
            }
			let qiuzhuzhuangtaiTypes = await this.$api.page(`dictionary`, qiuzhuzhuangtaiTypesParams);
			this.qiuzhuzhuangtaiTypesOptions = qiuzhuzhuangtaiTypes.data.list


            // 如果是更新操作
            if (options.id) {
                this.ruleForm.id = options.id;
                // 获取信息
                let res = await this.$api.info(`dongwu`, this.ruleForm.id);
                this.ruleForm = res.data;
            }
            if(options.dongwuId){
                this.ruleForm.dongwuId = options.dongwuId;
            }
            // 跨表
            // this.styleChange()
        },
        methods: {
            yonghuChange(e) {
                this.yonghuIndex = e.target.value
                this.ruleForm.yonghuName = this.yonghuOptions[this.yonghuIndex].yonghuName
                this.ruleForm.yonghuId = this.yonghuOptions[this.yonghuIndex].id
            },
            // 下拉变化
            dongwuTypesChange(e) {
                this.dongwuTypesIndex = e.target.value
                this.ruleForm.dongwuValue = this.dongwuTypesOptions[this.dongwuTypesIndex].indexName
                this.ruleForm.dongwuTypes = this.dongwuTypesOptions[this.dongwuTypesIndex].codeIndex
            },
            qiuzhuzhuangtaiTypesChange(e) {
                this.qiuzhuzhuangtaiTypesIndex = e.target.value
                this.ruleForm.qiuzhuzhuangtaiValue = this.qiuzhuzhuangtaiTypesOptions[this.qiuzhuzhuangtaiTypesIndex].indexName
                this.ruleForm.qiuzhuzhuangtaiTypes = this.qiuzhuzhuangtaiTypesOptions[this.qiuzhuzhuangtaiTypesIndex].codeIndex
            },
            // styleChange() {
            // 	this.$nextTick(() => {
            // 		// document.querySelectorAll('.app-update-pv .cu-form-group .uni-yaoxianStyle-yaoxianStyle').forEach(el=>{
            // 		//   el.style.backgroundColor = this.addUpdateForm.yaoxianStyle.content.backgroundColor
            // 		// })
            // 	})
            // },
			dongwuPhotoTap() {
                let _this = this;
                this.$api.upload(function(res) {
                    _this.ruleForm.dongwuPhoto = 'upload/' + res.file;
                    _this.$forceUpdate();
                    _this.$nextTick(() => {
                        // _this.styleChange()
                    })
                });
            },
			// 日期控件
			insertTimeConfirm(val) {
                this.ruleForm.insertTime = val.result;
                this.$forceUpdate();
            },
			// 日期控件
			createTimeConfirm(val) {
                this.ruleForm.createTime = val.result;
                this.$forceUpdate();
            },

            getUUID() {
                return new Date().getTime();
            },
            async onSubmitTap() {
				if ((!this.ruleForm.dongwuName)) {
                    this.$utils.msg(`求助名称不能为空`);
                    return
                }
				if ((!this.ruleForm.dongwuPhoto)) {
                    this.$utils.msg(`求助照片不能为空`);
                    return
                }
				if ((!this.ruleForm.dongwuClicknum) && (!this.$validate.isIntNumer(this.ruleForm.dongwuClicknum)) && this.ruleForm.dongwuClicknum >0) {
                    this.$utils.msg(`求助热度不能为空，不能小于0 格式为数字`);
                    return
                }
				if ((!this.ruleForm.qiuzhuzhuangtaiTypes)) {
                    this.$utils.msg(`求助状态不能为空`);
                    return
                }
                // if(uni.getStorageSync('详情页传过来的Id')){
                //     this.ruleForm.zhuangxiuId = uni.getStorageSync('详情页传过来的Id')
                // }

                if (this.ruleForm.id) {
                    await this.$api.update(`dongwu`, this.ruleForm);
                } else {
                    await this.$api.save(`dongwu`, this.ruleForm);
                }
                uni.setStorageSync('pingluenStateState', true);
                this.$utils.msgBack('提交成功');
            },
            getDate(type) {
                const date = new Date();
                let year = date.getFullYear();
                let month = date.getMonth() + 1;
                let day = date.getDate();
                if (type === 'start') {
                    year = year - 60;
                } else if (type === 'end') {
                    year = year + 2;
                }
                month = month > 9 ? month : '0' + month;;
                day = day > 9 ? day : '0' + day;
                return `${year}-${month}-${day}`;
            },
            toggleTab(str) {
                this.$refs[str].show();
            }
        }
    }
</script>
